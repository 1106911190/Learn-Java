public class FirstInit{
	public static void main(String[] args) {
		byte[] 	 	b = new byte[1];
		char[]  	c = new char[1];
		int[] 		i = new int[1];
		double[]	d = new double[1];

		System.out.println("b[0] = " + b[0]);
		System.out.println("c[0] = " + c[0]);
		System.out.println("i[0] = " + i[0]);
		System.out.println("d[0] = " + d[0]);
	}
}